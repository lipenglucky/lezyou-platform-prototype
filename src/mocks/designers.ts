function buildPortfolio(specialty: keyof typeof PORTFOLIO_BANK, prefix: string, categories: string[]) {
  return PORTFOLIO_BANK[specialty].map((cover, i) => ({
    id: `${prefix}-p${i + 1}`,
    category: categories[i % categories.length],
    title: `${categories[i % categories.length]} · 项目编号 ${100 + i}`,
    cover,
    year: 2024 - (i % 3),
  }));
}

function buildCalendar(seed: number) {
  const slots = [];
  const today = new Date("2026-05-01");
  for (let i = 0; i < 60; i++) {
    const d = new Date(today);
    d.setDate(d.getDate() + i);
    const day = d.getDay();
    const isWeekend = day === 0 || day === 6;
    const available = !isWeekend && (i + seed) % 7 !== 3 && (i + seed) % 11 !== 5;
    slots.push({ date: d.toISOString().slice(0, 10), available });
  }
  return slots;
}

